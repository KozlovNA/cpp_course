///////////////////////////////////////////////////////////////////////////////////////////

#include <cstddef>
#include <type_traits>

///////////////////////////////////////////////////////////////////////////////////////////

template < typename T                  > class is_function          : public std::false_type {};

template < typename T, typename ... Ts > class is_function < T(Ts...) > : public std:: true_type {};

///////////////////////////////////////////////////////////////////////////////////////////

template < typename T > constexpr auto is_function_v = is_function < T > ::value;

///////////////////////////////////////////////////////////////////////////////////////////

int main()
{

	static_assert(     is_function_v < int(int,int) > == 1);

	static_assert(     is_function_v < int    > == 0);

//  -----------------------------------------------
}

